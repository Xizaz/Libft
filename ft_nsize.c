#include "libft.h"

int	ft_nsize(int n)
{
	int size;

	size = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n = -n;
		size++;
	}
	while (n > 0)
	{
		size++;
		n = n / 10;
	}
	return (size);
}
